from ortools.sat.python import cp_model
import numpy as np

def parse_input(text):
    lines = text.strip().split('\n')
    N = int(lines[0])
    Q = int(lines[1])
    constraints = []
    for line in lines[2:]:
        parts = line.split()
        A = int(parts[0])
        B = int(parts[1])
        indices = list(map(int, parts[2:-1]))
        C = parts[-1]
        constraints.append((A, B, *indices, C))
    return N, Q, constraints

def create_csp_model(N, constraints):
    model = cp_model.CpModel()

    # 数字の範囲を定義
    lower_bound = 1
    upper_bound = 500

    # N個の変数を定義
    A = [model.NewIntVar(lower_bound, upper_bound, f"A_{i}") for i in range(N)]

    # 与えられた制約をモデルに追加
    for constraint in constraints:
        A_count, B_count, *indices, rel = constraint
        A_indices = indices[:A_count]
        B_indices = indices[A_count:A_count + B_count]

        # 集合Aと集合Bの和を計算
        A_sum = sum(A[i] for i in A_indices)
        B_sum = sum(A[i] for i in B_indices)

        if rel == '>':
            model.Add(A_sum > B_sum)
        elif rel == '<':
            model.Add(A_sum < B_sum)
        elif rel == '=':
            model.Add(A_sum == B_sum)

    return A, model

def exponential_cdf(x, lambd=0.01):
    """指数分布の累積分布関数"""
    return 1 - np.exp(-lambd * x)

def add_exponential_constraints(model, variables, lambd=1e-2, intervals=10):
    N = len(variables)
    max_val = (1e2*N/20)
    step = max_val / intervals
    
    all_sum = sum(variables[i] for i in range(N))
    model.Add(all_sum == 100*N)

    # 累積分布関数を利用して各区間の変数の数を計算
    for i in range(intervals):
        lower_bound = int(i * step)
        upper_bound = int((i + 1) * step)
        expected_fraction = exponential_cdf(upper_bound, lambd) - exponential_cdf(lower_bound, lambd)
        expected_count = int(N * expected_fraction)

        # 各区間に含まれる変数の数に制約を追加
        indicators = [model.NewBoolVar(f"indicator_{j}_{i}") for j, var in enumerate(variables)]
        for j, var in enumerate(variables):
            model.Add(var >= lower_bound).OnlyEnforceIf(indicators[j])
            model.Add(var <= upper_bound).OnlyEnforceIf(indicators[j])
        model.Add(sum(indicators) == expected_count)

def main(input_text):
    N, Q, constraints = parse_input(input_text)
    A, model = create_csp_model(N, constraints)
    add_exponential_constraints(model, A)

    solver = cp_model.CpSolver()
    status = solver.Solve(model)
    
    val = []
    if status == cp_model.OPTIMAL or status == cp_model.FEASIBLE:
        for i in range(N):
            print(f"A_{i} =", solver.Value(A[i]))
            val.append(solver.Value(A[i]))
    else:
        print("No solution found!")
    print(val)

